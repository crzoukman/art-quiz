function hideReviewIcons() {
  const reviews = document.querySelectorAll('.pictures-review');

  for (let i = 1; i <= 12; i++) {
    const res = localStorage.getItem(`picturesCategoryResult-${i}`);

    if (res === null) {
      reviews[i - 1].classList.add('vis-hide');
    } else {
      reviews[i - 1].classList.remove('vis-hide');
    };
  };

};

function showNonPlayed() {
  const images = document.querySelectorAll('.category-card-img');

  for (let i = 1; i <= 12; i++) {
    const res = localStorage.getItem(`picturesCategoryResult-${i}`);

    if (res === null) {
      images[i - 1].classList.add('black-white');
    } else {
      images[i - 1].classList.remove('black-white');
    };
  };
};

// --------------- event callbacks
function goToPicturesReview(e) {
  const id = e.target.dataset.id;
  const data = JSON.parse(localStorage.getItem('picturesResult'));
  if (data === null) return;
  if (data[id].length === 0) {

    return;
  };

  observable2.state.lastPicturesReviewID = id;

  window.location.hash = '#pictures-review';
};

function prepareDataForQuiz(e) {
  const id = Number(e.target.parentNode.dataset.id);
  observable2.state.currentPicturesPlayingCategory = id;
  const startFrom = (id - 1) * 10 + 120;
  const chunk = [];

  for (let i = startFrom; i < id * 10 + 120; i++) {
    chunk.push(data[i]);
  };

  observable2.setState({ currentPicturesQList: chunk });

};

function prepareAnswersData() {
  function randomInteger(min, max) {
    let rand = min + Math.random() * (max + 1 - min);
    return Math.floor(rand);
  };

  const answers = {
    '1': null,
    '2': null,
    '3': null,
    '4': null,
  };

  (() => {
    const qnum = randomInteger(1, 4);
    for (const key in answers) {
      if (key == qnum) {
        const qList = observable2.state.currentPicturesQList;
        const current = observable2.state.currentPicturesQuestion;
        answers[key] = Number(qList[current].imageNum);
        observable2.state.currentPicturesTrueAnswer = qList[current];
        // добавим в стейт айди правильного ответа
        observable2.state.currentPicturesAnswerID = qnum;

        return;
      };
    };


  })();

  (() => {

    for (const key in answers) {
      if (!answers[key]) {
        const value = (function unique() {
          const value = randomInteger(0, 240);

          if (Number(data[value].imageNum) === Number(observable2.state.currentPicturesTrueAnswer.imageNum)) {
            return unique();
          };

          for (const i in answers) {
            if (answers[i] === Number(data[value].imageNum)) {
              return unique();
            };
          };

          return value;
        })();

        answers[key] = Number(data[value].imageNum);
      };
    };
  })();

  observable2.setState({ currentPicturesBtnsPh: answers });
};

function prepareState() {
  const cur = observable2.state.currentPicturesPlayingCategory;

  // чистим стейт текущей играемой категории
  //observable2.state.picturesResult[cur].length = 0;
  observable2.state.picturesResult[cur] = [];


  // при клике на категорию указываем, что игра началась
  observable2.state.isPicturesCategoryPlaying = true;
};

